package methodOverloading;

public class FourTransaction {
	
	public int addition(int num1,int num2) {
		return num1+num2;
	}
	
	/*Overloading işlemi : Aynı metot ismini farklı şekillerde kullanılabilecek şekilde kodlayabiliriz*/
	
	public int addition(int num1,int num2,int num3) {
		return num1+num2+num3;
	}
	
	
	

}
